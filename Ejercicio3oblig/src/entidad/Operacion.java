
package entidad;


public class Operacion {
    
    private int Num1;
    private int num2;

    public Operacion() {
    }

    
    public Operacion(int Num1, int num2) {
        this.Num1 = Num1;
        this.num2 = num2;
    }

    public int getNum1() {
        return Num1;
    }

    public void setNum1(int Num1) {
        this.Num1 = Num1;
    }

    public int getNum2() {
        return num2;
    }

    public void setNum2(int num2) {
        this.num2 = num2;
    }
    
    
    
}
